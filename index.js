		function updatePageDots() {
		  const dots = [
		    root.dot_1,
		    root.dot_2,
		    root.dot_3
		  ];
		
		
		  // 1. Hide all dots initially (and safely)
		  dots.forEach((dot) => {
		    if (dot) {
		      dot.visible = false;
		    }
		  });
		
		  // 2. Only show dots if there's more than one slice
		  if (window.totalSlices <= 1) {
		    return;
		  }
		
		  // 3. Loop through slices, but never exceed the physical dots we have
		  for (let i = 0; i < window.totalSlices; i++) {
		    // Check if the dot object actually exists at this index
		    if (dots[i]) {
		      dots[i].visible = true;
		
		      if (i < window.currentSlice) {
		        // Completed page
		        dots[i].gotoAndStop(2);
		      } 
		      else if (i === window.currentSlice) {
		        // Current page
		        dots[i].gotoAndStop(1);
		      } 
		      else {
		        // Future page
		        dots[i].gotoAndStop(0);
		      }
		    } else {
		      // Logic safety: we have more slices than physical dots on stage
		      console.warn("Warning: More slices (" + window.totalSlices + ") than pagination dots available.");
		      break; 
		    }
		  }
		}
		
		
