		function snapToDrop(item, drop) {
		    const drag = item.drag;
		
		    if (drag.currentDrop) {
		        drag.currentDrop.occupied = false;
		        drag.currentDrop.currentItem = null;
		    }
		
		    // 1. Force state before measuring
		    drag.gotoAndStop(1); // Move to dropped frame first
		    drag.scaleX = drag.scaleY = 1; // Reset scale to 100%
		
		    // 2. Use nominalBounds (Animate's baked-in original size) 
		    // This is safer than getBounds() which changes when scaled
		    const bounds = drag.nominalBounds; 
		    const dropBounds = drop.nominalBounds;
		
		    if (bounds && dropBounds) {
		        // Calculate scale based on the original library size vs the drop zone size
		        const ratioW = dropBounds.width / bounds.width;
		        const ratioH = dropBounds.height / bounds.height;
		        const finalScale = Math.min(ratioW, ratioH) * 0.92;
		
		        drag.scaleX = drag.scaleY = finalScale;
		    } else {
		        // Fallback if bounds fail
		        drag.scaleX = drag.scaleY = 0.6; 
		    }
		
		    // 3. Set position
		    drag.x = drop.x + DRAG_OFFSET_X - 1.5;
		    drag.y = drop.y + DRAG_OFFSET_Y;
		
		    // 4. Update the logical links
		    drop.occupied = true;
		    drop.currentItem = drag;
		    drag.currentDrop = drop;
		
		    // 5. Fix text after a tiny delay to allow the frame to catch up
		    setTimeout(() => {
		        centerTextVertically(drag.drag_txt, 112, 360);
		        fixIOSText(drag.drag_txt, 20, true);
		    }, 50);
		
		    drag.removeAllEventListeners();
		}
		
