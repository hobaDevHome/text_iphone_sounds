		function getNearestFreeDrop(drag, drops) {
		  let best = null;
		  let bestDist = Infinity;
		
		  drops.forEach((drop) => {
		    if (drop.occupied || !drop.visible) return;
		
		    const dx = drag.x - drop.x;
		    const dy = drag.y - drop.y;
		
		    const dist = Math.sqrt(dx * dx + dy * dy);
		    // // console.log("Drop:", drop.name, "| occupied:", drop.occupied, "| dist:", dist.toFixed(0));
		
		    if (dist < bestDist) {
		      bestDist = dist;
		      best = drop;
		    }
		  });
		
		  // // console.log("✅ Best drop chosen:", best?.name);
		  return best;
		}
		
